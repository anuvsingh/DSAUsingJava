public class Relational {
    public static void main (String args[]) {

        // RELATIONAL OPERATORS
        // It gives ouput in true or false.
        System.out.println("Equal to Operator");
        int a = 10;
        int b = 10;
        System.out.println(a==b);
        System.out.println("Not Equal to Operator");
        int c = 5;
        int d = 9;
        System.out.println(c!=d);
        System.out.println("Greater than Operator");
        int e = 5;
        int f = 9;
        System.out.println(e>f);
        System.out.println("Smaller than Operator");
        int g = 5;
        int h = 9;
        System.out.println(g<h);
        System.out.println("Greater than equal to Operator");
        int i = 5;
        int j = 9;
        System.out.println(i>=j);
        System.out.println("Samller than equal to Operator");
        int k = 5;
        int l = 9;
        System.out.println(k<=l);
    }
}